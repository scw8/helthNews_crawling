LANGUAGE_GUIDE = """
- "어르신" 호칭 금지 → "여러분", "선생님" 사용
- 문장은 짧게. 한 문장에 정보 하나. 핵심은 2회 이상 반복.
- 의학 용어는 반드시 쉬운 말로 재설명 (괄호 안에 원어 병기)
- 구어체로 작성 (Vrew AI 음성으로 읽힘)
- 숫자는 구체적으로: "적당히" → "하루 30분", "일주일에 3번"
- 영상 자막 크기 고려: 한 자막당 최대 2줄
- 90~120초마다 반드시 시청 유지 장치(퀴즈/반전/예고) 삽입
- 훅은 반드시 반전형/위협형/질문형 중 하나로 작성. 평범한 사실 나열 금지.
"""


def build(topic: str, articles: list[dict]) -> str:
    """
    수집된 기사들을 Claude 프롬프트로 조립.
    """
    # 참고 자료 섹션 구성
    references = ""
    for i, article in enumerate(articles, 1):
        source_label = SOURCE_LABELS.get(article.get("source", ""), article.get("source", ""))
        title = article.get("title", "")
        content = article.get("content", "")
        # 본문이 너무 길면 앞 1000자만 사용
        content_preview = content[:1000] + "..." if len(content) > 1000 else content

        references += f"""
[참고 자료 {i}]
출처: {source_label}
제목: {title}
내용: {content_preview}
"""

    prompt = f"""당신은 55~70세 시니어를 대상으로 한 건강 유튜브 채널의 전문 작가입니다.

## 오늘의 주제
{topic}

## 언어 및 톤 가이드
{LANGUAGE_GUIDE}

## 영상 구성 (총 15분 기준)
{SCRIPT_STRUCTURE}

## 참고 자료 ({len(articles)}건)
아래 공신력 있는 자료를 바탕으로 스크립트를 작성하세요.
내용을 그대로 옮기지 말고, 시니어가 이해하기 쉽게 재구성하세요.
{references}

## 작성 지침
1. 위 구성 형식을 반드시 따르고 각 섹션 앞에 [훅 - 0:00] 형태로 타임코드를 표시하세요.
2. 훅은 반드시 반전형/위협형/질문형 중 하나로 작성하세요. "○○는 ~하다"는 평범한 사실 나열로 시작하지 마세요.
3. 원인 설명 파트는 1분 30초(약 200단어) 안에 끝내세요. 하나의 비유만 사용하세요.
4. ★ 표시된 시청 유지 장치는 반드시 해당 위치에 삽입하세요. 빠뜨리면 안 됩니다.
5. 핵심 정보는 5가지로, 시작 전에 "3번째가 제일 중요합니다"라고 예고하세요.
6. 스크립트 맨 끝에 참고한 출처 목록을 추가하세요.
7. 마지막 줄에 반드시 면책 고지를 포함하세요:
   "본 영상의 내용은 일반적인 건강 정보이며, 개인의 건강 상태에 따라 다를 수 있습니다. 구체적인 치료나 복약은 반드시 담당 의사와 상담하세요."

이제 스크립트를 작성해주세요.
"""
    return prompt
